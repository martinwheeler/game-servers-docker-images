#!/bin/bash
set -e

# Simple Terraria server install + run script for the container
# the Terraria binary writes worlds/config files in the working directory
# (which is ${HOMEDIR}/terraria).  mount this folder from the host to
# persist worlds and let the server upgrade itself.
mkdir -p "${HOMEDIR}/terraria" "${HOMEDIR}/terraria/Worlds" || true
# (legacy) keep ~/.local/share/Terraria around in case the binary looks there
mkdir -p "${HOMEDIR}/.local/share/Terraria" || true

# Ensure permissions are correct on the data directories
chmod -R 755 "${HOMEDIR}/terraria" 2>/dev/null || true
chmod -R u+w "${HOMEDIR}/terraria" 2>/dev/null || true
# make sure steam user owns it (especially if using a named volume)
chown -R "${USER}:${USER}" "${HOMEDIR}/terraria" 2>/dev/null || true

cd "${HOMEDIR}"

# The image build should always set TERRARIA_VERSION.
if [ -z "${TERRARIA_VERSION:-}" ]; then
  echo "TERRARIA_VERSION is required but not set" >&2
  exit 1
fi
if [ -z "${TERRARIA_URL}" ]; then
  TERRARIA_URL="https://terraria.org/api/download/pc-dedicated-server/terraria-server-${TERRARIA_VERSION}.zip"
fi

echo "Downloading Terraria server from ${TERRARIA_URL}"
wget -qO terraria.zip "${TERRARIA_URL}"
unzip -o terraria.zip -d terraria_tmp

# Move Linux binaries into place (zip layout may vary by version)
linux_dir=$(find terraria_tmp -type d -name Linux -print -quit || true)
if [ -n "${linux_dir}" ]; then
  mv "${linux_dir}"/* "${HOMEDIR}/terraria/" || true
else
  # fallback: move everything
  mv terraria_tmp/* "${HOMEDIR}/terraria/" || true
fi

chmod +x "${HOMEDIR}/terraria"/TerrariaServer.bin.x86_64* || true
rm -rf terraria.zip terraria_tmp

cd "${HOMEDIR}/terraria"

# generate config file from environment variables
config_file="serverconfig.txt"
# if the user didn't supply a worldname, build a unique one based on timestamp
if [ -z "$TERRARIA_WORLDNAME" ]; then
  TERRARIA_WORLDNAME="ServerTime Realm $(date +%s)"
fi
cat > "$config_file" <<-EOF
# generated by entry.sh
$( [ -n "$TERRARIA_WORLD" ] && printf 'world=%s
' "$TERRARIA_WORLD" )
$( [ -n "$TERRARIA_PORT" ] && printf 'port=%s
' "$TERRARIA_PORT" )
$( [ -n "$TERRARIA_MAXPLAYERS" ] && printf 'maxplayers=%s
' "$TERRARIA_MAXPLAYERS" )
$( [ -n "$TERRARIA_PASSWORD" ] && printf 'password=%s
' "$TERRARIA_PASSWORD" )
$( [ -n "$TERRARIA_WORLDNAME" ] && printf 'worldname=%s
' "$TERRARIA_WORLDNAME" )
$( [ -n "$TERRARIA_AUTOCREATE" ] && printf 'autocreate=%s
' "$TERRARIA_AUTOCREATE" )

EOF

# Run the 64-bit server binary using config file, append any additional args
echo "Starting Terraria server with config $config_file..."
cmd=("./TerrariaServer.bin.x86_64" -config "$config_file")
# add nogui flag on the command line instead of writing it to config
if [ -n "$TERRARIA_NOGUI" ]; then
  cmd+=("-nogui")
fi
# append any additional args last
if [ -n "$ADDITIONAL_ARGS" ]; then
  # shellcheck disable=SC2206
  cmd+=( $ADDITIONAL_ARGS )
fi
"${cmd[@]}"
